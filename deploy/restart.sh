#!/bin/bash

# 湘佳电商重启脚本
# 用途：重启后台管理系统和移动端API服务

set -e

# ==================== 配置区域 ====================
INSTALL_DIR="/opt/xj-shop"

# ==================== 主程序 ====================
main() {
    echo ""
    echo "=========================================="
    echo "   湘佳电商系统重启脚本"
    echo "=========================================="
    echo ""

    # 停止服务
    ${INSTALL_DIR}/stop.sh

    echo ""
    echo "等待3秒..."
    sleep 3
    echo ""

    # 启动服务
    ${INSTALL_DIR}/start.sh
}

main